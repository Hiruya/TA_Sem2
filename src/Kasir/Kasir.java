    private void jKodePesananPopupMenuWillBecomeVisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jKodePesananPopupMenuWillBecomeVisible
        // TODO add your handling code here:
        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
        String query = "SELECT Kode_Pesanan FROM data_produk ORDER BY Kode_Pesanan ASC";
        try (PreparedStatement statement = KoneksiDB.con.prepareStatement(query)) {
            ResultSet resultSet = statement.executeQuery();

            while (resultSet.next()) {
                String kodePesanan = resultSet.getString("Kode_Pesanan");
                comboBoxModel.addElement(kodePesanan);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        jKodePesanan.setModel(comboBoxModel);
        jKodePesanan.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String selectedKodePesanan = (String) jKodePesanan.getSelectedItem();

                String query = "SELECT Nama_Pesanan, Harga_Satuan FROM data_produk WHERE Kode_Pesanan = ?";
                try (PreparedStatement statement = KoneksiDB.con.prepareStatement(query)) {
                    statement.setString(1, selectedKodePesanan);
                    ResultSet resultSet = statement.executeQuery();

                    if (resultSet.next()) {
                        String namaPesanan = resultSet.getString("Nama_Pesanan");
                        String hargaSatuan = resultSet.getString("Harga_Satuan");
                        jNamaPesanan.setText(namaPesanan);
                        jHargaSatuan.setText(hargaSatuan);
                    } else {
                        jNamaPesanan.setText("");
                        jHargaSatuan.setText("");
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        });
    }//GEN-LAST:event_jKodePesananPopupMenuWillBecomeVisible

