    public void id_auto() {
        ResultSet rs = KoneksiDB.ambilData("SELECT * FROM kasir ORDER BY No_Transaksi DESC");
        try {
            if (rs.next()) {
                String id = rs.getString("No_Transaksi");
                int idNum;
                if (id != null && !id.isEmpty()) {
                    try {
                        idNum = Integer.parseInt(id.replaceAll("[^0-9]", ""));
                        idNum++;
                    } catch (NumberFormatException e) {
                        // Handle the case where the No_Transaksi field is not in the expected format
                        idNum = 1;
                    }
                } else {
                    // Handle the case where the No_Transaksi field is empty
                    idNum = 1;
                }
                String result = String.valueOf(idNum);
                jNoTransaksi.setText(result);
            } else {
                // Handle the case where the result set is empty
                jNoTransaksi.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private String UbahFormatJCalendar(java.sql.Date abc) {
        SimpleDateFormat dtFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            return dtFormat.format(abc);
        } catch (Exception e) {
            System.err.println("Error:" + e.getMessage());
            return " ";
        }
    }

