    private void jTambahDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTambahDataActionPerformed
        String No_Transaksi = jNoTransaksi.getText();
        String Nama_Customer = jNamaCustomer.getText();
        String No_Telepon = jNoTelepon.getText();
        String Kode_Pesanan = jKodePesanan.getSelectedItem().toString();
        String Nama_Pesanan = jNamaPesanan.getText();
        String Total_Pesanan = jTotalPesanan.getText();
        String Harga_Satuan = jHargaSatuan.getText();
        String Jumlah_Harga = jJumlahHarga.getText();
        String Total_Harga = jTotalHarga.getText();
        String Note = jNote.getText();
        java.sql.Date Tanggal_Penerimaan = new java.sql.Date(jCalendar.getDate().getTime());

        if (jNamaCustomer.getText().length() <= 0
                || jNoTelepon.getText().length() <= 0
                || jKodePesanan.getSelectedItem() == null
                || jNamaPesanan.getText().length() <= 0
                || jTotalPesanan.getText().length() <= 0
                || jHargaSatuan.getText().length() <= 0
                || jJumlahHarga.getText().length() <= 0
                || jTotalHarga.getText().length() <= 0
                || jNote.getText().length() <= 0
                || jCalendar.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Masukkan Data yang Kurang");
            id_auto();
        } else {
            KoneksiDB.aksi("INSERT INTO `kasir`(`No_Transaksi`, `Nama_Customer`, `No_Telepon`, `Kode_Pesanan`, `Nama_Pesanan`, `Total_Pesanan`, `Harga_Satuan`, `Total_Harga`, `Note`, `Tanggal_Penerimaan`, `Jumlah_Harga`) VALUES ('" + No_Transaksi + "','" + Nama_Customer + "','" + No_Telepon + "','" + Kode_Pesanan + "','" + Nama_Pesanan + "','" + Total_Pesanan + "','" + Harga_Satuan + "','" + Total_Harga + "','" + Note + "','" + Tanggal_Penerimaan + "','" + Jumlah_Harga + "')");

            jNoTransaksi.setText("");
            jNamaCustomer.setText("");
            jNoTelepon.setText("");
            jKodePesanan.setSelectedItem("");
            jNamaPesanan.setText("");
            jTotalPesanan.setText("");
            jHargaSatuan.setText("");
            jTotalHarga.setText("");
            jJumlahHarga.setText("");
            jNote.setText("");
            jCalendar.setDate(null);
            AturBaris();

            id_auto();
            JOptionPane.showMessageDialog(null, "Data Ditambahkan");
        }
    }//GEN-LAST:event_jTambahDataActionPerformed

    private void jHitungDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHitungDataActionPerformed
        // TODO add your handling code here:
        if (!"".equals(jHargaSatuan.getText())
                && !"".equals(jTotalPesanan.getText())) {
            int harga = Integer.parseInt(jHargaSatuan.getText());
            int jumlah = Integer.parseInt(jTotalPesanan.getText());
            int subTotal = harga * jumlah;
            jTotalHarga.setText(Integer.toString(subTotal));
            jJumlahHarga.setText(Integer.toString(subTotal));
        } else {
            JOptionPane.showMessageDialog(null, "Anda harus mengisi data dengan lengkap", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jHitungDataActionPerformed

